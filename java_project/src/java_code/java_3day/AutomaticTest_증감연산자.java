package java_code.java_3day;

public class AutomaticTest_증감연산자 {

	public static void main(String[] args) {

		int i = 10;
		System.out.println("i = " + i); // 10
		System.out.println("i++ = " + i++); // 10
		System.out.println("i = " + i); // 11
		System.out.println("++i = " + ++i); // 12
		System.out.println("--i = " + --i); // 11
		System.out.println("i-- = " + i--); // 11
		System.out.println("i = " + i); // 10
	}

}
