package java_code.java_03day;

public class AutomaticTest_산술연산자 {

	public static void main(String[] args) {
		int x = 100;
		int y = 200;
		
		System.out.println("x + y = " + (x+y));
		System.out.println("x - y = " + (x-y));
		System.out.println("x * y = " + (x*y));
		System.out.println("x / y = " + (x/y));
		System.out.println("x % y = " + (x%3)); // 짝수, 홀수 구분시 사용

	}

}
