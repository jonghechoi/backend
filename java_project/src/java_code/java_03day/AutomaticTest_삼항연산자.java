package java_code.java_3day;

public class AutomaticTest_삼항연산자 {

	public static void main(String[] args) {

		// 삼항연산자 : 조건식? 참:거짓
		// 비교연산자를 사용하여 조건식을 작성

		System.out.println((5>3)? "true":"false");
		System.out.println((5<3)? "true":"false");
		System.out.println((5==3)? "true":"false");
		System.out.println((5!=3)? "true":"false");
	}

}
