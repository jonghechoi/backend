package java_code.java_03day;

public class AutomaticTest_비교연산자 {

	public static void main(String[] args) {
		int x = 10;
		int y = 8;
		
		System.out.println("x > y = " + (x>y));
		System.out.println("x < y = " + (x<y));
		System.out.println("x == y = " + (x==y));
		System.out.println("x != y = " + (x!=y));

	}

}
