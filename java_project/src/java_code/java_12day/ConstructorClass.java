package java_code.java_12day;

public class ConstructorClass {
	// Field
	String name, address;
	int age;
	
	// Constructor
	// 기본생성자가 호출되면 홍길동, 서울시, 30 <= 이 데이터로 초기화 진행
	// 리턴타입 메소드명() {} <= 이것이 기본 메소드 구조인데 생성자는 리턴타입 없음
	ConstructorClass() {
		this.name = "홍길동";
		this.address = "서울시";
		this.age = 30;
	}
	
	
	// Method
}
